#include "ven.h"

/**
 * add - add 2 integers
 * @a: inte
 * @b: int
 * Return: a + b
 */

int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - sub 2 nums
 * @a: int
 * @b: int
 * Return: a - b
 */

int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiply 2 nums
 * @a: int
 * @b: int
 * Return: mul
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - division 2 nums
 * @a: int
 * @b: int
 * Return: a / b
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - mod 2 nums
 * @a: int
 * @b: int
 * Return: a % b
 */
int mod(int a, int b)
{
	return (a % b);
}

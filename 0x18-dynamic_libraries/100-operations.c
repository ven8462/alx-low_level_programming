#include "ven.h"

/**
 * add - add 2 integers
 * @a: 1st integer
 * @b: 2nd integer
 * Return: a + b
 */

int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - sub 2 nums
 * @a: 1st integer
 * @b: 2nd integer
 * Return: a - b
 */

int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiply 2 nums
 * @a: 1st integer
 * @b: 2nd integer
 * Return: mul
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - division 2 nums
 * @a: 1st num
 * @b: 2nd num
 * Return: a / b
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - mod 2 nums
 * @a: 1st num
 * @b: 2nd num
 * Return: a % b
 */
int mod(int a, int b)
{
	return (a % b);
}
